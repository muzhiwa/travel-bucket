// Function to load sample data into localStorage for new users
export const loadSampleData = () => {
  try {
    const existingData = localStorage.getItem("travelDestinations");
    if (!existingData || JSON.parse(existingData).length === 0) {
      localStorage.setItem(
        "travelDestinations",
        JSON.stringify(sampleDestinations)
      );
      return sampleDestinations;
    }
    return JSON.parse(existingData);
  } catch (error) {
    return [];
  }
};

// Function to check if user is new (has no destinations)
export const isNewUser = () => {
  try {
    const existingData = localStorage.getItem("travelDestinations");
    return !existingData || JSON.parse(existingData).length === 0;
  } catch (error) {
    return true;
  }
};
